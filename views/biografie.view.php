                Kort na de geboorte van Lovelace gaan haar ouders uit elkaar. Haar vader verliet Engeland voorgoed, dus ze leerde hem nooit persoonlijk kennen. <br><br>Haar moeder stimuleerde haar in de exacte wetenschappen, naar horen en zeggen:

Lovelace raakte sinds 1833 geinteresseerd in Babbage’s machines nadat zij door 
Mary Somerville en  een bekende wiskundige en wetenschapscommunicator werd voorgesteld aan Charles Babbage. Charles was de ontwerper van de <a href=”https://www.britannica.com/technology/Analytical-Engine”> Analytical Engine </a>, een mechanische machine die wordt beschouwd als een voorloper van de moderne computer.<br><br>


Ada’s grootste bijdrage kwam toen zij in 1843 een artikel van de Italiaanse ingenieur Luigi Menabrea vertaalde over Babbage’ machine. Aan deze vertaling voegde zij uitgebreide eigen notities toe, die uiteindelijk veel langer waren dan het oorspronkelijke artikel. In deze notities beschreef zij een methode om de machine getallen te laten berekenen. <br><br>


Wat Ada bijzonder maakte, was haar visie: zij begreep dat computers in de toekomst niet alleen berekeningen zouden uitvoeren, maar ook muziek, tekst en afbeeldingen zouden kunnen verwerken. Deze gedachte was haar tijd ver vooruit.
<br><br>
